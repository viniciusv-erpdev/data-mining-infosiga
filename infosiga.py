def analisar_logradouros_json(df, coluna='logradouro', threshold=85):
    # 1. Pré-processamento e Contagem
    df_temp = df.copy()
    df_temp[coluna] = df_temp[coluna].apply(normalizar_texto)

    contagem = df_temp[coluna].value_counts().to_dict()
    nomes_unicos = list(contagem.keys())

    relatorio_json = []
    ja_processados = set()

    # Ordena pelos mais frequentes para garantir que o "certo" seja o líder do grupo
    nomes_ordenados = sorted(nomes_unicos, key=lambda x: contagem[x], reverse=True)

    for nome in nomes_ordenados:
        if nome in ja_processados or not nome:
            continue

        # Busca similares
        similares = process.extract(nome, nomes_unicos, scorer=fuzz.token_sort_ratio)

        # Filtra variações
        variacoes = [
            {
                "variacao": res[0],
                "frequencia": contagem[res[0]],
                "score_similaridade": res[1]
            }
            for res in similares
            if res[1] >= threshold and res[0] != nome and res[0] not in ja_processados
        ]

        if variacoes:
            # Estrutura o grupo de erro
            grupo = {
                "nome_principal_sugerido": nome,
                "frequencia_total_principal": contagem[nome],
                "variacoes_detectadas": variacoes
            }
            relatorio_json.append(grupo)

            # Marca todos do grupo como processados
            for v in variacoes:
                ja_processados.add(v["variacao"])

        ja_processados.add(nome)

    return relatorio_json

