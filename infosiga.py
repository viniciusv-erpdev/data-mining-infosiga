def analisar_logradouros(df, coluna='logradouro', threshold=85):
    # Limpeza básica: remove duplicatas exatas e valores nulos
    nomes_unicos = df[coluna].dropna().unique().tolist()

    erros_encontrados = []
    ja_processados = set()

    for nome in nomes_unicos:
        if nome in ja_processados:
            continue

        # Busca nomes similares que não sejam o próprio nome
        similares = process.extract(nome, nomes_unicos, scorer=fuzz.token_sort_ratio)

        # Filtra apenas os que têm alta similaridade mas não são idênticos
        possiveis_erros = [res[0] for res in similares if res[1] >= threshold and res[0] != nome]

        if possiveis_erros:
            # Registra o grupo de erros
            linha = f"Principal: {nome} | Possíveis variações ({len(possiveis_erros)}): {', '.join(possiveis_erros)}"
            erros_encontrados.append(linha)

            # Adiciona ao set para não repetir a análise com as variações
            for p in possiveis_erros:
                ja_processados.add(p)
            ja_processados.add(nome)

    return erros_encontrados

